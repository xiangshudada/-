package com.easylink.ruleproduct.core.model;

public enum OperatorType {
    /**
     * 客观事实描述类算子。
     *
     * 适用于“确认交易情况”“确认客户基本信息”等不直接判断疑点的步骤，
     * 主要把数据需求查询出的事实整理为可写入报告的描述。
     */
    OBJECTIVE_DESCRIBE,

    /**
     * 单指标阈值比较算子。
     *
     * 适用于金额、笔数、数量、年龄等单个指标与固定阈值比较的规则，
     * 例如“近一年转入金额 >= 100 万”。
     */
    THRESHOLD_COMPARE,

    /**
     * 比例类比较算子。
     *
     * 适用于两个或多个指标计算比率后再比较阈值的规则，
     * 例如“银证转账金额 / 证券交易金额 >= 10”。
     */
    RATIO_COMPARE,

    /**
     * 时间间隔类比较算子。
     *
     * 适用于判断两个事件之间间隔是否过短、过长或落入指定窗口的规则，
     * 例如“开户后短期内发生大额交易”。
     */
    TIME_INTERVAL_COMPARE,

    /**
     * 时序模式识别算子。
     *
     * 适用于需要按时间顺序识别连续行为或组合行为的规则，
     * 例如“短期集中买入后快速卖出”。
     */
    SEQUENCE_PATTERN,

    /**
     * 价格偏离算子。
     *
     * 适用于成交价与收盘价、均价、估值或市场基准价之间的偏离判断，
     * 例如“大宗交易成交价较前一日收盘价偏离超过阈值”。
     */
    PRICE_DEVIATION,

    /**
     * 关系匹配算子。
     *
     * 适用于判断客户与关联账户、交易对手或相关主体之间是否存在关系，
     * 例如相同联系方式、相同设备、股权关系、受益所有人关系等。
     */
    RELATION_MATCH,

    /**
     * 交易对手匹配算子。
     *
     * 适用于客户与交易对手之间交易行为的匹配性分析，
     * 例如对敲交易中的委托数量、成交时间、证券标的是否高度一致。
     */
    COUNTERPARTY_MATCH,

    /**
     * 身份资产不匹配算子。
     *
     * 适用于客户身份、收入、注册资本、经营规模与账户资产或资金流入
     * 明显不匹配的规则。
     */
    IDENTITY_ASSET_MISMATCH,

    /**
     * 客户画像风险检查算子。
     *
     * 适用于基于客户画像字段判断风险特征的规则，
     * 例如年龄异常、职业敏感、职业模糊、收入区间异常等。
     */
    PROFILE_RISK_CHECK,

    /**
     * 人工核查算子。
     *
     * 适用于系统无法自动判断、需要客户经理回访或人工补充材料的步骤。
     * 该类型通常返回待人工处理状态，并保留已查询事实作为核查依据。
     */
    MANUAL_REVIEW
}
